<h4>
    The goal of a logo is to use colors, graphics, type and photos to
    <br/>
    create a single lasting image that captures the essence of a business.
</h4>
<div class="mainWeb">
    <p class="bold">The goal is to put your logo in places customers are likely to see it, and reinforce that recognition pattern with multiple viewings.</p>
    <p>A corporate logo is a graphical identity that speaks volumes about your business in the market. It is a symbol that is a visual representative of the organization. It conveys the core values and essence of the company. Another important function of a logo is to create a memorable impression on the minds of viewers. It enables the viewer to easily associate the product with the company. The symbol should also be easily recognizable and provoke a favorable response in the minds of the customers.</p>
    <p>The logo is a symbol that provides the customers with instant and powerful brand recognition of your business and the products or services that the company offers, but it is also critical to ensure that the logo sends across the right message to the customer to maintain a consistent brand image and increase customer recognition as a weak message could weaken your overall marketing strategy.</p>
    <p>A logo can also clearly reflect that message the brand wants to convey, therefore a strong association between the brand and the logo is inevitable. A logo should not only reflect growth and professionalism no matter how small the company is today, but also ensure that the logo one selects is not dated but can be used effectively year after year keeping in mind how consumers would perceive the company in order to be continually associated with it in the times to come.</p>
</div>
