    <h4>
        Companies get huge profits through the publicity that television
        <br/>
        commercials bring. Don't you think it is time for you to invest in one?
    </h4>
    <p class="bold">The TV commercial is generally considered the most effective mass-market advertising format.</p>
    <p>Always dreamed of having your own TV commercial to play in your local area, but didn't have the big production budget to make it a reality?</p>
    <p>Assured Performance is proud to offer a set of ready-made commercials suitable for television broadcast. We have filmed and produced 10 different commercials suitable for broadcast on TV, the internet, even in-movie-theater advertising. They're now available to you, our valued Co-Op Program Members. These commercials were filmed in stunning HD but will work on standard televisions as well.</p>
    <p>The production was done in such a way that the shots are generic...by watching the commercials, you cannot tell what shop you are at. By utilizing creative camera angles, you get a big-budget commercial at a much more affordable rate.</p>
    <p>Of course, we will integrate imagery of your shop as well. There are two ways to do this: send us some video of the front of your shop or send us a few still images of your shop.</p>
    <p>Your video will be output in the proper format based on where you are broadcasting it. All commercial spots are :30 seconds in length.</p>
    <p>
        <span class="bold">We have developed these commercials in two categories:</span>
        <br/>
        For those who are part of the APN VIP Program and for those who are not. All commercial spots can be customized to include video or still images of YOUR shop. Your address and website will also be integrated.
    </p>
    <p>
        <span class="bold">VIP Commercials</span>
        make specific mention of your acceptance into APN's exclusive VIP Program. Used as a powerful endorsement, the benefits to the customer are briefly outlined and integrated into these commercials. The voiceover in each of these videos is temporary: it can be replaced with any number of voices - male or female, old or young - utilizing the exact same script.
    </p>
